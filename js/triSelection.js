let arraySelected = [10,7,6,2,5,4,1,3,8,9];

for(let i=0; i<arraySelected.length; i++){
    let mi = i;

    for(let j = i +1; j<arraySelected.length; j++){
        if(arraySelected[j] < arraySelected[mi]){
            mi = j;
        }
    }

    let temp = arraySelected[i];
    arraySelected[i] = arraySelected[mi];
    arraySelected[mi] = temp;
}
